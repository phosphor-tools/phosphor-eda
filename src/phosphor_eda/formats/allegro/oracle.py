"""Reusable Allegro sidecar and manufacturing-output oracle helpers.

These helpers parse independent evidence generated by Cadence or downstream
manufacturing tools. They validate parser output in later Allegro slices; they
do not parse native ``.brd`` data and must not become a loader fallback.
"""

from __future__ import annotations

import re
import shutil
import subprocess
from dataclasses import dataclass
from enum import StrEnum
from typing import TYPE_CHECKING

if TYPE_CHECKING:
    from pathlib import Path

KICAD_CONVERSION_TIMEOUT_SECONDS = 120


class OracleEvidenceKind(StrEnum):
    IPC356 = "ipc356"
    EXCELLON_DRILL = "excellon_drill"
    GERBER_MANIFEST = "gerber_manifest"
    PACKAGED_NETLIST = "packaged_netlist"
    PLACEMENT = "placement"
    KICAD_CONVERSION = "kicad_conversion"


ALLEGRO_ORACLE_CAPABILITIES = frozenset(
    {
        "component_identity",
        "connectivity",
        "constraints",
        "drills",
        "geometry",
        "placement",
        "placement_count",
        "stackup",
    }
)


@dataclass(frozen=True, slots=True)
class OracleCoverage:
    validates: frozenset[str]
    cannot_prove: frozenset[str]


ALLEGRO_ORACLE_COVERAGE: dict[OracleEvidenceKind, OracleCoverage] = {
    OracleEvidenceKind.IPC356: OracleCoverage(
        validates=frozenset({"connectivity", "geometry", "stackup"}),
        cannot_prove=ALLEGRO_ORACLE_CAPABILITIES
        - frozenset({"connectivity", "geometry", "stackup"}),
    ),
    OracleEvidenceKind.EXCELLON_DRILL: OracleCoverage(
        validates=frozenset({"drills", "geometry"}),
        cannot_prove=ALLEGRO_ORACLE_CAPABILITIES - frozenset({"drills", "geometry"}),
    ),
    OracleEvidenceKind.GERBER_MANIFEST: OracleCoverage(
        validates=frozenset({"geometry", "stackup", "drills"}),
        cannot_prove=ALLEGRO_ORACLE_CAPABILITIES - frozenset({"geometry", "stackup", "drills"}),
    ),
    OracleEvidenceKind.PACKAGED_NETLIST: OracleCoverage(
        validates=frozenset({"connectivity", "component_identity"}),
        cannot_prove=ALLEGRO_ORACLE_CAPABILITIES
        - frozenset({"connectivity", "component_identity"}),
    ),
    OracleEvidenceKind.PLACEMENT: OracleCoverage(
        validates=frozenset({"placement_count"}),
        cannot_prove=ALLEGRO_ORACLE_CAPABILITIES - frozenset({"placement_count"}),
    ),
    OracleEvidenceKind.KICAD_CONVERSION: OracleCoverage(
        validates=frozenset({"connectivity", "geometry", "stackup"}),
        cannot_prove=ALLEGRO_ORACLE_CAPABILITIES
        - frozenset({"connectivity", "geometry", "stackup"}),
    ),
}


@dataclass(frozen=True, slots=True)
class Ipc356Layer:
    name: str
    material: str
    layer_type: str
    film_type: str
    number: int


@dataclass(frozen=True, slots=True)
class Ipc356TestRecord:
    record_type: str
    net_name: str
    refdes: str
    pin: str
    x_mils: float | None
    y_mils: float | None
    access_side: str

    @property
    def is_via(self) -> bool:
        return self.refdes == "VIA"


@dataclass(frozen=True, slots=True)
class Ipc356Report:
    path: Path
    version: str
    units: str
    decimal_places: int
    layers: tuple[Ipc356Layer, ...]
    test_records: tuple[Ipc356TestRecord, ...]

    @property
    def net_names(self) -> frozenset[str]:
        return frozenset(record.net_name for record in self.test_records if record.net_name)

    @property
    def component_refs(self) -> frozenset[str]:
        return frozenset(
            record.refdes for record in self.test_records if record.refdes and not record.is_via
        )


@dataclass(frozen=True, slots=True)
class DrillTool:
    code: str
    diameter_inch: float


@dataclass(frozen=True, slots=True)
class DrillHit:
    tool_code: str
    raw_x: str
    raw_y: str


@dataclass(frozen=True, slots=True)
class ExcellonDrill:
    path: Path
    units: str
    zero_suppression: str
    tools: tuple[DrillTool, ...]
    hits: tuple[DrillHit, ...]

    @property
    def hit_counts_by_tool(self) -> dict[str, int]:
        counts = {tool.code: 0 for tool in self.tools}
        for hit in self.hits:
            counts[hit.tool_code] = counts.get(hit.tool_code, 0) + 1
        return counts


